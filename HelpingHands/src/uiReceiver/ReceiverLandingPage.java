    private void btnTrackFundsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrackFundsActionPerformed
        // TODO add your handling code here:
        ResultSet resultSet = receiverDirectory.getReceiver(loggedInUser.split("-")[0].trim());
        try {
            if (!resultSet.isBeforeFirst() ) {
                System.out.println("No data");
                return;
            }   } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "No Causes Available for this Category");
        }
        try {
            while(resultSet.next()){
            ReceiverTrackCause receiverTrackCause = new ReceiverTrackCause(Integer.valueOf(resultSet.getString("ID")));
            cardLayoutPanel.removeAll();
            cardLayoutPanel.add(receiverTrackCause);
            cardLayoutPanel.repaint();
            cardLayoutPanel.revalidate();            
            }
        
        } catch (SQLException ex) {
            Logger.getLogger(ReceiverAssignCause.class.getName()).log(Level.SEVERE, null, ex);
        }            


        
    }//GEN-LAST:event_btnTrackFundsActionPerformed

